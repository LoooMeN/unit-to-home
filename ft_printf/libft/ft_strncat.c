#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	i;
	size_t	a;

	i = 0;
	a = 0;
	while (s1[i])
		i++;
	while (a < n && s2[a])
	{
		s1[i] = s2[a];
		a++;
		i++;
	}
	s1[i] = '\0';
	return (s1);
}
