#include "libft.h"

void	ft_putnbr(int n)
{
	if (n < 0)
	{
		if (n <= -2147483648)
			write(1, "-2147483648", 11);
		else
		{
			n = -n;
			write(1, "-", 1);
		}
	}
	if (n <= 9 && n >= 0 && n >= 0)
		ft_putchar(n + '0');
	else if (n >= 0)
	{
		ft_putnbr(n / 10);
		ft_putchar(n % 10 + '0');
	}
}
